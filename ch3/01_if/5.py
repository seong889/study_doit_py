# and / or / not

v1 = True
v2 = False
print("v1 = True, v2 = False")

print("v1 or v2")
if v1 or v2:
    print("True")
else:
    print("False")

print("v1 and v2")
if v1 and v2:
    print("True")
else:
    print("False")

print("v1 or not v2")
if v1 or not v2:
    print("True")
else:
    print("False")
